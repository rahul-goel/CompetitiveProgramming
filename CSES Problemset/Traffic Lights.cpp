int solve() {
    int x, n;
    cin >> x >> n;
    set < int > light;
    multiset < int > track;
    track.insert(x - 0);
    light.insert(0), light.insert(x);

    for (int i = 0; i < n; i++) {
        int a;
        cin >> a;

        auto r = light.upper_bound(a);
        auto l = prev(r);

        track.erase(track.find(*r - *l));
        track.insert(*r - a), track.insert(a - *l);
        light.insert(a);

        cout << *--track.end() << " ";
    }
    cout << endl;

    return 0;
}

signed main() {
    fastio;

    int t = 1;
    while (t--) {
        solve();
    }
    return 0;
}
